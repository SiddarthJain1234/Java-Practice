public class Solution1 {
    public static void main(String args[]) {

        int i = 5;
        switch(i) {

            case 1:
                System.out.print("Case 1" + " ");
                break;

            case 2:
                System.out.print("Case 2" + " ");
                break;

            case 3: 
                System.out.print("Case 3" + " ");
                break;

            default: 
                System.out.print("Value of i is greater than 3" + " ");
        }
    } 
}